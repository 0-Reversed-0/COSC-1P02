public class Switches
{

    public Switches()
    {

        /**
         * Switch statements
         * these wil run all the cases until a break
         */
       
        int x = 0;
        // switch( )
        // {
        // case:
        // code
        // case:
        // code
        // case:
        // code
        // }

        switch (x)
        {
            case 0:
                System.out.println("0");                
            case 1:
                System.out.println("1");
            case 2:
                System.out.println("2");
                break;
            default:
                System.out.println("Invalid");

        }

    }

}